#include <iostream>
using namespace std;
main()
{
	int m = 4;
	int n = 8;
	if(m==n) cout<<m<<" sama dengan "<<n<<endl;
	else if(m!=n) cout<<m<<"  tidak sama dengan "<<n<<endl;
	else if(m>n) cout<<m<<"  lebih besar dari "<<n<<endl;
	else if(m<n) cout<<m<<"  lebih kecil dari "<<n<<endl;
	return 0;
}
