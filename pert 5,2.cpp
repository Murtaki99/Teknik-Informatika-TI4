#include <iostream>
using namespace std;
 main()
{
	int m =82;
	int n = 26;
	cout<<m<<"<< 2"<<" = "<<(m<<2)<<endl;
	cout<<m<<">> 2"<<" = "<<(m>>2)<<endl;
	cout<<m<<" &"<<n<<" = "<<(m&n)<<endl;
	cout<<m<<" | "<<n<<" = "<<(m|n)<<endl;
	cout<<m<<" ^ "<<n<<" = "<<(m^n)<<endl;
	cout<<"~"<<m<<" = "<<~m<<endl;
}
